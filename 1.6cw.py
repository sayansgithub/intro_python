import turtle
screen = turtle.Screen() 
screen.bgcolor("light blue")
screen.setup(800, 500) 

tom=turtle.Turtle()
tom.penup()
tom.goto(0,-300)
tom.pendown()
tom.color('red')
tom.begin_fill()
tom.circle(200)
tom.end_fill()

tom.color('orange')
tom.begin_fill()
tom.circle(180)
tom.end_fill()

tom.color('yellow')
tom.begin_fill()
tom.circle(160)
tom.end_fill()

tom.color('green')
tom.begin_fill()
tom.circle(140)
tom.end_fill()

tom.color('blue')
tom.begin_fill()
tom.circle(120)
tom.end_fill()

tom.color('purple')
tom.begin_fill()
tom.circle(100)
tom.end_fill()

tom.color('light blue')
tom.begin_fill()
tom.circle(80)
tom.end_fill()

screen.reset()




























